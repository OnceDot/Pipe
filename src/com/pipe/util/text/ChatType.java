package com.pipe.util.text;

public enum ChatType {

    CHAT(0),
    SYSTEM(1),
    GAME_INFO(2);

    ///////////////////////////////////////////////////////////////////////////

    private int id;

    ///////////////////////////////////////////////////////////////////////////

    ChatType(int id) {
        this.id = id;
    }

    ///////////////////////////////////////////////////////////////////////////

    public int getId() {
        return id;
    }
}
